public class Variables {
    public static void main(String[] args) {
        int octal = 012;
        int decimal = 1975;
        int binary = 0b1110;
        long hex = 0xFA;
        System.out.println(binary);
        System.out.println(octal);
        System.out.println(decimal);
        System.out.println(hex);
        double x = 6.3;
        int y = 10;
        System.out.println(x+y);
        int z = 3/6 ;
        System.out.println(z);
    }
}
